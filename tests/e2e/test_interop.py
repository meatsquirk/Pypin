class TestCBORInterop:
    """Test CBOR serialization compatibility."""

    def test_cbor_map_key_ordering(self):
        """CBOR maps should use consistent key ordering for signatures."""
        # This is critical for canonical CBOR (RFC 8949 Section 4.2.1)
        hello = Hello(
            version="1.0.0",
            node_id=b"\x01\x02\x03\x04",
            capabilities=["guardian"],
            collections=["eth:0x1234"],
            timestamp=1000,
            user_agent="test",
        )

        cbor_bytes = cbor2.dumps(hello.to_dict())
        decoded = cbor2.loads(cbor_bytes)

        # Keys should be preserved (including version)
        assert set(decoded.keys()) == {"version", "node_id", "capabilities", "collections", "timestamp", "user_agent"}

    def test_bytes_encoding(self):
        """Bytes should be encoded as CBOR byte strings (major type 2)."""
        data = {"node_id": b"\x01\x02\x03\x04"}
        cbor_bytes = cbor2.dumps(data)

        # Decode and verify type
        decoded = cbor2.loads(cbor_bytes)
        assert isinstance(decoded["node_id"], bytes)

    def test_integer_encoding_sizes(self):
        """Integers should use appropriate CBOR encoding sizes."""
        test_cases = [
            0,
            23,  # 1 byte (immediate)
            24,  # 2 bytes
            255,
            256,  # 3 bytes
            65535,
            65536,  # 5 bytes
            4294967295,
            4294967296,  # 9 bytes
        ]

        for value in test_cases:
            data = {"value": value}
            cbor_bytes = cbor2.dumps(data)
            decoded = cbor2.loads(cbor_bytes)
            assert decoded["value"] == value

    def test_float_encoding(self):
        """Float coverage values should serialize correctly."""
        test_cases = [0.0, 0.5, 1.0, 0.85, 0.999]

        for value in test_cases:
            data = {"coverage": value}
            cbor_bytes = cbor2.dumps(data)
            decoded = cbor2.loads(cbor_bytes)
            assert abs(decoded["coverage"] - value) < 0.0001

    def test_optional_fields_omission(self):
        """Optional fields set to None should be omitted from CBOR."""
        hello = Hello(
            version="1.0.0",
            node_id=b"\x01\x02\x03\x04",
            capabilities=[],
            collections=[],
            timestamp=1000,
            user_agent=None,  # Optional, should be omitted
        )

        hello_dict = hello.to_dict()
        assert "user_agent" not in hello_dict

    def test_nested_structures(self):
        """Nested CBOR structures should serialize correctly."""
        announce_collections = [
            CollectionAnnouncement(
                id="eth:0x1234",
                manifest_cid="QmTest",
                coverage=1.0,
                shard_ids=[0, 1, 2],
            ),
        ]

        for coll in announce_collections:
            cbor_bytes = cbor2.dumps(coll.to_dict())
            decoded = cbor2.loads(cbor_bytes)

            assert decoded["id"] == "eth:0x1234"
            assert decoded["shard_ids"] == [0, 1, 2]


