class TestRFCCompliance:
    """Test compliance with DCPP RFC requirements."""

    def test_max_message_size_enforced(self):
        """Messages exceeding 32MB should be rejected (RFC Section 5.5)."""
        from dcpp_python.framing import MessageTooLargeError

        # Just over 32MB
        large_payload = b"x" * (MAX_MESSAGE_SIZE + 1)

        with pytest.raises(MessageTooLargeError):
            Profile1Framer.encode(MessageType.MANIFEST, large_payload)

        with pytest.raises(MessageTooLargeError):
            Profile1Framer.encode(MessageType.MANIFEST, large_payload)

    def test_challenge_length_max(self):
        """Challenge length should be clamped to 1024 (RFC Section 6.8)."""
        challenge = Challenge(cid="QmTest", offset=0, length=2048)

        # Length is clamped on construction for safety
        assert challenge.length == 1024  # Clamped to MAX_LENGTH

        # And serialization also uses clamped value
        challenge_dict = challenge.to_dict()
        assert challenge_dict["length"] == 1024

    def test_challenge_validated_construction(self):
        """Challenge.new_validated should validate against item size."""
        # Valid: offset + clamped_length <= item_size
        challenge = Challenge.new_validated("QmTest", offset=500, length=2000, item_size=2000)
        assert challenge.length == 1024  # Clamped
        assert challenge.offset == 500

        # Invalid: offset + clamped_length > item_size
        with pytest.raises(ValueError, match="Invalid offset"):
            Challenge.new_validated("QmTest", offset=500, length=1000, item_size=1000)

    def test_announce_rate_limit_fields(self):
        """ANNOUNCE should include all required replay protection fields."""
        collections = [CollectionAnnouncement(
            id="eth:0x1234",
            manifest_cid="QmTest",
            coverage=1.0,
        )]

        timestamp = int(time.time())
        announce = Announce(
            node_id=b"\x01\x02\x03\x04",
            announce_seq=timestamp,
            collections=collections,
            timestamp=timestamp,
            expires_at=timestamp + 3600,
            signature=b"\x00" * 64,
        )

        announce_dict = announce.to_dict()

        # RFC Section 6.3.1 - Required fields for replay protection
        assert "announce_seq" in announce_dict
        assert "timestamp" in announce_dict
        assert "expires_at" in announce_dict
        assert "signature" in announce_dict

    def test_profile1_header_size_20_bytes(self):
        """Profile 1 header should be 20 bytes (RFC Section 5.1).

        Header format: Magic(4) + Version(2) + Type(2) + RequestID(4) + Length(4) + CRC32(4) = 20
        """
        payload = b"test"
        frame = Profile1Framer.encode(MessageType.HELLO, payload)

        # Total frame = header + payload
        assert len(frame) == 20 + len(payload)


