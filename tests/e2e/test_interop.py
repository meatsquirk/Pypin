class TestProfile1Interop:
    """Test Profile 1 framing interoperability."""

    def test_magic_bytes(self):
        """Magic bytes should be 0x44435050 ('DCPP')."""
        payload = b"\xa0"
        frame = Profile1Framer.encode(MessageType.HELLO, payload)

        assert frame[:4] == b"DCPP"
        assert struct.unpack(">I", frame[:4])[0] == 0x44435050

    def test_version_bytes(self):
        """Version bytes (offset 4-5) should be 0x0100 for DCPP v1.0."""
        payload = b"\xa0"
        frame = Profile1Framer.encode(MessageType.HELLO, payload)

        assert frame[4:6] == b"\x01\x00"

    def test_message_type_offset(self):
        """Message type should be at offset 6-7, big-endian."""
        for msg_type in MessageType:
            payload = b"\xa0"
            frame = Profile1Framer.encode(msg_type, payload)

            encoded_type = struct.unpack(">H", frame[6:8])[0]
            assert encoded_type == msg_type.value

    def test_length_offset(self):
        """Payload length should be at offset 12-15, 4-byte big-endian."""
        test_payloads = [b"", b"x", b"x" * 1000, b"x" * 100000]

        for payload in test_payloads:
            frame = Profile1Framer.encode(MessageType.MANIFEST, payload)

            length = struct.unpack(">I", frame[12:16])[0]
            assert length == len(payload)

    def test_crc32c_offset(self):
        """CRC32C should be at offset 16-19, big-endian."""
        payload = b"test payload for CRC"
        frame = Profile1Framer.encode(MessageType.HELLO, payload)

        # Extract CRC from frame
        frame_crc = struct.unpack(">I", frame[16:20])[0]

        # Calculate expected CRC
        expected_crc = crc32c(payload)

        assert frame_crc == expected_crc

    def test_crc32c_known_vectors(self):
        """CRC32C should match known test vectors."""
        # CRC-32C (Castagnoli) test vectors
        test_vectors = [
            (b"", 0x00000000),
            (b"123456789", 0xE3069283),
            (b"The quick brown fox jumps over the lazy dog", 0x22620404),
        ]

        for data, expected in test_vectors:
            actual = crc32c(data)
            assert actual == expected, \
                f"CRC32C mismatch for {data!r}: expected {expected:#010x}, got {actual:#010x}"

    def test_header_size(self):
        """Profile 1 header should be exactly 20 bytes."""
        payload = b"test"
        frame = Profile1Framer.encode(MessageType.HELLO, payload)

        # Header + payload
        assert len(frame) == 20 + len(payload)

    def test_crc_verification_failure(self):
        """Corrupted CRC should be detected."""
        payload = b"test payload"
        frame = bytearray(Profile1Framer.encode(MessageType.HELLO, payload))

        # Corrupt the CRC (at offset 16-19)
        frame[16] ^= 0xFF

        with pytest.raises(ChecksumError):
            Profile1Framer.decode(bytes(frame))

    def test_payload_corruption_detected(self):
        """Payload corruption should be detected via CRC."""
        payload = b"test payload"
        frame = bytearray(Profile1Framer.encode(MessageType.HELLO, payload))

        # Corrupt the payload (last byte)
        frame[-1] ^= 0xFF

        with pytest.raises(ChecksumError):
            Profile1Framer.decode(bytes(frame))


