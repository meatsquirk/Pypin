def generate_test_vectors_json() -> str:
    """
    Generate test vectors as JSON for cross-implementation testing.

    This function creates a JSON file with known-good message bytes
    that can be used to validate other implementations.
    """
    vectors = []

    for tv in TEST_VECTORS:
        payload = cbor2.dumps(tv.payload_dict)
        p1_frame = Profile1Framer.encode(tv.message_type, payload)

        vectors.append({
            "name": tv.name,
            "message_type": tv.message_type.value,
            "payload": tv.payload_dict,
            "cbor_hex": payload.hex(),
            "profile1_hex": p1_frame.hex(),
        })

    return json.dumps(vectors, indent=2, default=_json_serializer)


def _json_serializer(obj):
    """JSON serializer for bytes objects."""
    if isinstance(obj, bytes):
        return {"$bytes": obj.hex()}
    raise TypeError(f"Object of type {type(obj)} is not JSON serializable")


