def ensure_test_vectors(path: Path) -> None:
    """
    Ensure Rust-generated test vectors exist for interop tests.

    These vectors are required for byte-for-byte compatibility checks.
    """
    if path.exists():
        return

    repo_root = Path(__file__).resolve().parents[2]
    print("[SETUP] Generating test vectors via cargo run --bin generate_test_vectors")
    with path.open("w", encoding="utf-8") as f:
        result = subprocess.run(
            ["cargo", "run", "--bin", "generate_test_vectors"],
            cwd=repo_root,
            stdout=f,
            stderr=subprocess.PIPE,
            text=True,
            timeout=300,
        )
    if result.returncode != 0:
        raise RuntimeError(
            "Failed to generate test vectors:\n"
            f"{result.stderr}"
        )
