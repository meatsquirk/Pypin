class TestMessageRoundtrip:
    """Test message serialization roundtrips."""

    @pytest.mark.parametrize("vector", TEST_VECTORS, ids=[v.name for v in TEST_VECTORS])
    def test_vector_roundtrip(self, vector: TestVector):
        """Test vectors should roundtrip correctly through encode/decode."""
        # Encode payload
        payload = cbor2.dumps(vector.payload_dict)

        frame = Profile1Framer.encode(vector.message_type, payload)
        decoded = Profile1Framer.decode(frame)
        assert decoded.message_type == vector.message_type
        assert cbor2.loads(decoded.payload) == vector.payload_dict

    def test_announce_with_signature_roundtrip(self):
        """ANNOUNCE with signature should preserve all fields."""
        from dcpp_python.crypto import generate_keypair, sign_message, derive_peer_id

        signing_key, verify_key = generate_keypair()
        node_id = derive_peer_id(verify_key)

        collections = [
            CollectionAnnouncement(
                id="eth:0x1234",
                manifest_cid="QmTest",
                coverage=1.0,
                shard_ids=[0, 1],
            ),
        ]

        timestamp = 1704067200
        expires_at = timestamp + 3600

        signable_dict = {
            "node_id": node_id,
            "announce_seq": timestamp,
            "collections": [c.to_dict() for c in collections],
            "timestamp": timestamp,
            "expires_at": expires_at,
        }

        signature = sign_message(signable_dict, signing_key)

        announce = Announce(
            node_id=node_id,
            announce_seq=timestamp,
            collections=collections,
            timestamp=timestamp,
            expires_at=expires_at,
            signature=signature,
        )

        # Roundtrip
        cbor_bytes = announce.to_cbor()
        restored = Announce.from_cbor(cbor_bytes)

        assert restored.node_id == node_id
        assert restored.signature == signature
        assert len(restored.collections) == 1
        assert restored.collections[0].id == "eth:0x1234"


